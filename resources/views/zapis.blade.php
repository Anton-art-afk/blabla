   <center> <h1>Запись в библиотеку</h1></center>
  
<p style="text-indent: 20px;">1. Для записи в библиотеку граждане предъявляют в информационную службу:</p>
<ul>
    <li>паспорт с отметкой о регистрации (или иной удостоверяющий личность документ при разовом посещении);</li>
    <li>военнослужащие – военный билет;</li>
    <li>студенты вузов и учащиеся средних специальных учебных заведений – дополнительно студенческий билет;</li>
    <li>учащиеся средних общеобразовательных школ – свидетельство о рождении или паспорт; несовершеннолетние на основании документов, удостоверяющих личность их законных представителей, знакомятся с Правилами пользования библиотекой.</li>
</ul>
<p style="text-indent: 40px;">2. На основании предъявленных документов, а также сведений о специальности и месте работы (учебы), оформляется регистрационная карточка, удостоверяющая факт записи гражданина в библиотеку, и выдается читательский билет – документ, дающий право пользования всеми услугами библиотеки.
</p>
<p style="text-indent: 40px;">3. Гражданам, имеющим постоянную регистрацию в г. Чите, а также жителям сельских поселений «Смоленское» и «Засопкинское» муниципального района «Читинский район» выдается постоянный читательский билет.</p>
<p style="text-indent: 40px;">4. Лицам, не имеющим или имеющим временную регистрацию в г. Чите, иностранным гражданам выдается временный читательский билет, который дает право пользоваться фондами только читальных залов в течение срока, указанного в билете.</p>
<p style="text-indent: 40px;">5. Пенсионерам, участникам ВОВ, инвалидам I–II группы, детям-сиротам, включая студентов очных отделений в возрасте до 23 лет, читательский билет оформляется без оплаты. </p>
<p style="text-indent: 40px;">6. Срок действия постоянного читательского билета – с 1 января по 31 декабря календарного года.</p>
<p style="text-indent: 40px;">7. Перерегистрация пользователей проводится один раз в год с 1 января.</p>

 
